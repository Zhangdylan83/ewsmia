document.addEventListener("DOMContentLoaded", function () {
    // Initialize Back-to-Top and Read More Buttons
    initializeBackToTop();
    initializeReadMore();

    // Initialize Carousel Navigation
    initializeCarouselNavigation();
});

/**
 * Initializes the Back-to-Top Button
 */
function initializeBackToTop() {
    const backToTop = document.getElementById("back-to-top");
    window.addEventListener("scroll", () => {
        backToTop.style.display = window.scrollY > 300 ? "block" : "none";
    });
    backToTop.addEventListener("click", () => {
        window.scrollTo({ top: 0, behavior: "smooth" });
    });
}

/**
 * Initializes the Read More Button
 */
function initializeReadMore() {
    const readMoreBtn = document.getElementById("read-more-btn");
    const moreText = document.getElementById("more-text");
    if (readMoreBtn && moreText) {
        readMoreBtn.addEventListener("click", () => {
            moreText.style.display = moreText.style.display === "none" ? "inline" : "none";
            readMoreBtn.textContent = moreText.style.display === "none" ? "Read More" : "Read Less";
        });
    }
}

/**
 * Initializes Carousel Navigation (Next and Previous Buttons)
 */
function initializeCarouselNavigation() {
    const images = document.querySelectorAll(".image-wrapper");
    const captions = [
        "Original histopathology image (left) and the corresponding heatmap generated by INSIGHT (right). The green outlines on the original image represent ground-truth regions. The bottom row provides zoomed-in views of the regions highlighted by yellow boxes.", // Original WSI
        "Original histopathology image (left) and the corresponding heatmap generated by INSIGHT (right). The green outlines on the original image represent ground-truth regions.", // Yellow Box
        "Additional Whole Slide Image heatmap visualizations generated by INSIGHT. The green outlines represent ground-truth regions.", // Zoomed-In Image
    ];
    const captionElement = document.getElementById("carousel-caption");
    const nextBtn = document.getElementById("next-btn");
    const prevBtn = document.getElementById("prev-btn");
    let currentIndex = 0;

    /**
     * Updates the image and caption based on the current index
     */
    function updateCarousel(index) {
        images.forEach((img, i) => {
            img.style.display = i === index ? "block" : "none";
        });
        captionElement.textContent = captions[index];
    }

    /**
     * Handles the next button click
     */
    function handleNextButton() {
        currentIndex = (currentIndex + 1) % images.length;
        updateCarousel(currentIndex);
    }

    /**
     * Handles the previous button click
     */
    function handlePrevButton() {
        currentIndex = (currentIndex - 1 + images.length) % images.length;
        updateCarousel(currentIndex);
    }

    // Attach event listeners to buttons
    if (nextBtn) {
        nextBtn.addEventListener("click", handleNextButton);
    }
    if (prevBtn) {
        prevBtn.addEventListener("click", handlePrevButton);
    }

    // Initialize the carousel to show the first image and caption
    updateCarousel(currentIndex);
}
